#///////////////////////CICLO WHILE///////////////////////////////
x = 0
limite = 0
while x < 160:
    limite += x
    x += 2
    print(f"la suma de los numero es de {x} y el total es de {limite}")



#/////////////////////CICLO FOR/////////////////////////////////
    
total = 0
for x in range(2,160,2):
    total += x 
    print(f"El numero es {x} y el total {total}")
